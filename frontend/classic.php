            <div class="Down" id="Down's">
                <h1>Down's Syndrome</h1>
                <div class="content">
                    <p><span>Down's Syndrome: </span>It is a condition that occurs when a person has an extra chromosome
                        called “trisomy 21.” This means that they have a total of 47 chromosomes instead of 46. This
                        extra
                        chromosome changes the development of the child’s brain and body. Which may cause mental and
                        physical problems.</p>
                </div>
                <div class="content2">
                    <ol>
                        <h2>Types of Down syndrome:</h2>
                        <li>People often cannot distinguish between each type without looking at chromosomes because the
                            physical traits and behaviors are similar. The types are:</li>
                        <li>Trisomy 21: About 95% of people with Down syndrome suffer from it, as each cell in the body
                            contains 3 separate copies of chromosome 21 instead of the normal two copies.</li>
                        <li>Mosaic Down Syndrome: This type affects about 2% of people with it. It means a mix, where
                            some
                            of their cells contain 3 copies of chromosome 21, but other cells have the typical 2 copies
                            of
                            chromosome 21.</li>
                    </ol>
                </div>

