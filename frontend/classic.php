                            as
                            a result of having a medical problem and a specific health condition.</li>
                        <li><span>Generalized anxiety disorder:</span> excessive anxiety about getting up
                            Any activity or involvement in any events, even routine ones</li>
                        <li><span>Panic disorder:</span> It is a series of anxiety and fear that reach their maximum
                            levels
                            within a few minutes. The person suffering from this type of anxiety may feel shortness of
                            breath, rapid breathing, and chest pain.</li>
                        <li><span>Selective mutism:</span> This is the failure of children to speak in specific
                            situations,
                            such as: being at school.</li>
                        <li><span>Separation anxiety:</span> It is a childhood disorder that is represented by fear and
                            anxiety of separation from parents. Social phobia: fear of engaging in social events,
